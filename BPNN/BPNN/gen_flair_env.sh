#SRC_DIR=$(pwd)

if [[ -z "${FLAIR_ROOT}" ]]; then
    echo "âŒ ERROR: FLAIR_ROOT not found"
    exit 1
fi
if [[ -z "$toolchains" ]]; then
    echo "âŒ ERROR: CMake Toolchain files not found'"
    exit 1
fi

echo "âœ… SDK and Toolchain validated."
